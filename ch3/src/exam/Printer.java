package exam;

public class Printer {
	//메소드 오버로딩
	public void println(int var) {
		System.out.println(var);
		
	}
	public void println(boolean var) {
		System.out.println(var);
		
	}
	public void println(double var) {
		System.out.println(var);
		
	}
	public void println(String var) {
		System.out.println(var);
		
	}
	
	//default 생성자는 있어도 되고 안써도 자동생성되니 없어도 됨.

}
